# Q3: Explain underfitting. List scenarios where underfitting can occur in ML.

# Answer - 

# Underfitting is defined as when the accuracy of the train data is less and at the time of testing the accuracy is also less. In the scenerio the 
# dataset is high bias and high variance 

# scenarios where underfitting can occur in ML : 

# Data used for training is not cleaned and contains noise (garbage values) in it.
# The model has a high bias.
# The size of the training dataset used is not enough.
# The model is too simple.