# Q4: Explain the bias-variance tradeoff in machine learning. What is the relationship between bias and
# variance, and how do they affect model performance?

# Answer - 

# The bias-variance tradeoff in machine learning involves finding the right balance between simplicity (bias) and complexity (variance) in a model. 
# High bias leads to underfitting and poor performance, while high variance leads to overfitting and also poor performance. 
# The goal is to strike a balance for optimal generalization to new data. Techniques like cross-validation and regularization help manage this tradeoff.