# Q6: Compare and contrast bias and variance in machine learning. What are some examples of high bias
# and high variance models, and how do they differ in terms of their performance?

# Answer - 

# The bias-variance in machine learning involves finding the right balance between simplicity (bias) and complexity (variance) in a model. 
# High bias leads to underfitting and poor performance, while high variance leads to overfitting and also poor performance. 
# The goal is to strike a balance for optimal generalization to new data. Techniques like cross-validation and regularization help manage this tradeoff