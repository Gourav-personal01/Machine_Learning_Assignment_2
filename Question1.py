# Q1: Define overfitting and underfitting in machine learning. What are the consequences of each, and how
# can they be mitigated?

# Answer 

# Overfitting is defined as when the accuracy of the train data is much more and at the time of testing the accuracy is very down. In the scenerio the 
# data set is low bias and high variance 

# Underfitting is defined as when the accuracy of the train data is less and at the time of testing the accuracy is also less. In the scenerio the 
# dataset is high bias and high variance 

# In order to mitigate this issue we should always consider that the accuracy of data should be always high while training and testing. we will called 
# this as Generalized Model and dataset is low bias and low variance.